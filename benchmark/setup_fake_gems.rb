#!/usr/bin/env ruby
# frozen_string_literal: true

# Generates a synthetic gem environment for benchmarking bootsnap.
#
# Usage:
#   ruby benchmark/setup_fake_gems.rb [num_gems] [files_per_gem] [target_dir]
#
# Defaults: 300 gems, 20 files each, in tmp/fake_gems
#
# Each gem gets:
#   lib/
#     gemname.rb           (entry point, requires subfiles)
#     gemname/
#       file_0.rb .. file_N.rb  (with cross-gem requires)

require "fileutils"

NUM_GEMS      = Integer(ARGV[0] || ENV.fetch("NUM_GEMS", 300))
FILES_PER_GEM = Integer(ARGV[1] || ENV.fetch("FILES_PER_GEM", 20))
TARGET_DIR    = ARGV[2] || ENV.fetch("TARGET_DIR", File.expand_path("../tmp/fake_gems", __dir__))

def generate_gems(num_gems:, files_per_gem:, target_dir:)
  FileUtils.rm_rf(target_dir)
  FileUtils.mkdir_p(target_dir)

  gem_names = num_gems.times.map { |i| format("fakegem_%04d", i) }
  load_paths = []

  gem_names.each_with_index do |name, gem_idx|
    lib_dir = File.join(target_dir, name, "lib")
    sub_dir = File.join(lib_dir, name)
    FileUtils.mkdir_p(sub_dir)
    load_paths << lib_dir

    # Entry point file
    requires = files_per_gem.times.map { |j| "require '#{name}/file_#{j}'" }.join("\n")
    File.write(File.join(lib_dir, "#{name}.rb"), <<~RUBY)
      # frozen_string_literal: true
      module #{camelize(name)}
        VERSION = "1.0.0"
      end
      #{requires}
    RUBY

    # Sub-files: each has some code and occasionally requires another gem
    files_per_gem.times do |j|
      # ~30% of files require a file from another random gem (simulates cross-gem deps)
      cross_require = ""
      if j % 3 == 0 && gem_idx > 0
        other_gem = gem_names[rand(gem_idx)]
        other_file = rand(files_per_gem)
        cross_require = "require '#{other_gem}/file_#{other_file}'\n"
      end

      File.write(File.join(sub_dir, "file_#{j}.rb"), <<~RUBY)
        # frozen_string_literal: true
        #{cross_require}module #{camelize(name)}
          module File#{j}
            DATA = #{j}
          end
        end
      RUBY
    end
  end

  # Write metadata for benchmark scripts to consume
  manifest_path = File.join(target_dir, "manifest.rb")
  File.write(manifest_path, <<~RUBY)
    # frozen_string_literal: true
    # Auto-generated by setup_fake_gems.rb
    FAKE_GEM_DIR = #{target_dir.inspect}
    FAKE_GEM_COUNT = #{num_gems}
    FAKE_FILES_PER_GEM = #{files_per_gem}
    FAKE_LOAD_PATHS = #{load_paths.inspect}
    FAKE_GEM_NAMES = #{gem_names.inspect}
    FAKE_TOTAL_FILES = #{num_gems * (files_per_gem + 1)}
  RUBY

  puts "Generated #{num_gems} gems × #{files_per_gem + 1} files = #{num_gems * (files_per_gem + 1)} total files"
  puts "Load path entries: #{load_paths.size}"
  puts "Target: #{target_dir}"
  puts "Manifest: #{manifest_path}"
end

def camelize(name)
  name.split("_").map(&:capitalize).join
end

generate_gems(num_gems: NUM_GEMS, files_per_gem: FILES_PER_GEM, target_dir: TARGET_DIR)
